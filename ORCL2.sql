-- MSL (My Star League) 전체 DB 초기화 스크립트
-- [통합본] ORCL2.sql + migration_pve_3v3_bo3.sql
--
-- 전제: DB를 완전히 초기화하고 새로 만든다는 가정하에, 기존 ORCL2.sql에
--       누적되어 있던 모든 ALTER/UPDATE 마이그레이션 결과를 각 테이블의
--       CREATE TABLE 정의에 "최종 상태"로 흡수(flatten)했다.
--       따라서 이 스크립트 한 번만 실행하면 기존 ORCL2.sql을 처음부터
--       끝까지 실행한 것과 동일한 최종 스키마 상태가 된다.
--
-- 이번에 반영된 3:3 BO3 변경사항 (migration_pve_3v3_bo3.sql 내용)
--   1) TBL_PVE_BATTLE_SESSION.SET_RESULTS_DATA CLOB 컬럼을
--      CREATE TABLE 단계부터 포함 (기존 ALTER 중복 제거)
--   2) 경기 체계를 "1세트/2세트/3세트(BO3)" 로 고정
--      - TBL_PVE_SUBSTAGE_MAPS : 세트별 맵 지정을 5개 → 3개로 축소
--      - TBL_PVE_OPPONENTS     : 구(舊) 5세트-1인1조(SET_NUMBER 1~5) 체계를
--        신(新) 3세트 x 3명 = 9슬롯(SET_NUMBER 1~9) 체계로 재배치
--        (1~3번째 슬롯=1세트 출전 3명, 4~6번째=2세트, 7~9번째=3세트)
-- =====================================================================

