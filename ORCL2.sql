-- 1. 사용자
CREATE TABLE TBL_USERS (
    USER_ID     VARCHAR2(50)  PRIMARY KEY,
    USER_PW     VARCHAR2(100) NOT NULL,
    USER_NICK   VARCHAR2(100) NOT NULL,
    CRYSTAL     NUMBER        DEFAULT 0,
    TEAM_NAME   VARCHAR2(100)
);

-- 2. 선수 마스터
CREATE TABLE TBL_PLAYERS (
    PLAYER_SEQ      NUMBER        CONSTRAINT PK_PLAYERS PRIMARY KEY,
    PLAYER_NAME     VARCHAR2(100) NOT NULL,
    RACE            CHAR(1)       NOT NULL CHECK (RACE IN ('T', 'P', 'Z')),
    RARITY          VARCHAR2(10)  NOT NULL CHECK (RARITY IN ('N', 'R', 'SR', 'SSR', 'UR')),
    STAT_ATTACK     NUMBER(3)     DEFAULT 50,
    STAT_DEFENSE    NUMBER(3)     DEFAULT 50,
    STAT_MACRO      NUMBER(3)     DEFAULT 50,
    STAT_MICRO      NUMBER(3)     DEFAULT 50,
    STAT_LUCK       NUMBER(3)     DEFAULT 50,
    PLAYER_IMG_URL  VARCHAR2(255),
    PLAYER_COST     NUMBER(3)     DEFAULT 10
);

-- 3. 시즌 팩
CREATE TABLE TBL_PACKS (
    PACK_SEQ        NUMBER        CONSTRAINT PK_PACKS PRIMARY KEY,
    PACK_NAME       VARCHAR2(100) NOT NULL UNIQUE,
    DESCRIPTION     VARCHAR2(500),
    COST_CRYSTAL    NUMBER        DEFAULT 0,
    BANNER_IMG_URL  VARCHAR2(255),
    IS_AVAILABLE    CHAR(1)       DEFAULT 'Y' CHECK (IS_AVAILABLE IN ('Y', 'N'))
);

-- 4. 팩 구성 선수 (확률표)
CREATE TABLE TBL_PACK_CONTENTS (
    PACK_CONTENT_SEQ NUMBER       CONSTRAINT PK_PACK_CONTENTS PRIMARY KEY,
    PACK_SEQ         NUMBER       NOT NULL CONSTRAINT FK_PACK_CONTENTS_PACK   REFERENCES TBL_PACKS(PACK_SEQ),
    PLAYER_SEQ       NUMBER       NOT NULL CONSTRAINT FK_PACK_CONTENTS_PLAYER REFERENCES TBL_PLAYERS(PLAYER_SEQ),
    DRAW_PROBABILITY NUMBER(7, 6) NOT NULL CHECK (DRAW_PROBABILITY > 0 AND DRAW_PROBABILITY <= 1),
    CONSTRAINT UK_PACK_PLAYER UNIQUE (PACK_SEQ, PLAYER_SEQ)
);

-- 5. 유저 보유 선수
CREATE TABLE TBL_OWNED_PLAYERS (
    OWNED_PLAYER_SEQ       NUMBER       CONSTRAINT PK_OWNED_PLAYERS PRIMARY KEY,
    USER_ID                VARCHAR2(50) NOT NULL CONSTRAINT FK_OWNED_PLAYER_USER   REFERENCES TBL_USERS(USER_ID),
    PLAYER_SEQ             NUMBER       NOT NULL CONSTRAINT FK_OWNED_PLAYER_MASTER REFERENCES TBL_PLAYERS(PLAYER_SEQ),
    CURRENT_ATTACK         NUMBER(3)    DEFAULT NULL,
    CURRENT_DEFENSE        NUMBER(3)    DEFAULT NULL,
    CURRENT_MACRO          NUMBER(3)    DEFAULT NULL,
    CURRENT_MICRO          NUMBER(3)    DEFAULT NULL,
    CURRENT_LUCK           NUMBER(3)    DEFAULT NULL,
    ACQUIRED_AT            DATE         DEFAULT SYSDATE,
    CURRENT_RARITY         VARCHAR2(10) NOT NULL,
    ACQUIRED_FROM_PACK_SEQ NUMBER       CONSTRAINT FK_OWNED_PLAYER_PACK REFERENCES TBL_PACKS(PACK_SEQ)
