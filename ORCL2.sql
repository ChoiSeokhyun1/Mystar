-- =====================================================================
-- ★ 14. 빌드 마스터 (수정된 핵심 테이블)
--    구버전(new2.sql)에는 USER_ID / PLAY_STYLE / EXPAND_STYLE / AGGRESSION / TECH_STYLE 없음
--    → 이 컬럼들이 없어서 insertBuild 시 ORA-00904 오류 발생
-- =====================================================================
--     PLAY_STYLE  : HARASS_FOCUS=견제 / DEFENSIVE=방어 / AGGRESSIVE=공격
--     EXPAND_STYLE: FAST_MULTI=패스트 / NORMAL_MULTI=일반 / LATE_MULTI=늦은
--     AGGRESSION  : MIN_MULTI=최소 / MID_MULTI=중간 / MAX_MULTI=최대
--     TECH_STYLE  : FAST_TECH=빠른(5분) / MID_TECH=중간(7분) / LATE_TECH=느린(10분)
CREATE TABLE TBL_BUILDS (
    BUILD_ID     NUMBER        CONSTRAINT PK_BUILDS PRIMARY KEY,
    USER_ID      VARCHAR2(50)  CONSTRAINT FK_BUILDS_USER REFERENCES TBL_USERS(USER_ID) ON DELETE CASCADE,
    BUILD_NAME   VARCHAR2(100) NOT NULL,
    RACE         CHAR(1)       NOT NULL CHECK (RACE IN ('T', 'P', 'Z')),
    VS_RACE      CHAR(1)       NOT NULL CHECK (VS_RACE IN ('T', 'P', 'Z', 'A')),
    PLAY_STYLE   VARCHAR2(15)  DEFAULT 'AGGRESSIVE' NOT NULL
                               CONSTRAINT CHK_BUILDS_PLAY_STYLE
                               CHECK (PLAY_STYLE IN ('HARASS_FOCUS', 'DEFENSIVE', 'AGGRESSIVE')),
    EXPAND_STYLE VARCHAR2(15)  DEFAULT 'NORMAL_MULTI' NOT NULL
                               CONSTRAINT CHK_BUILDS_EXPAND_STYLE
                               CHECK (EXPAND_STYLE IN ('FAST_MULTI', 'NORMAL_MULTI', 'LATE_MULTI')),
    AGGRESSION   VARCHAR2(15)  DEFAULT 'MID_MULTI' NOT NULL
                               CONSTRAINT CHK_BUILDS_AGGRESSION
                               CHECK (AGGRESSION IN ('MIN_MULTI', 'MID_MULTI', 'MAX_MULTI')),
    TECH_STYLE   VARCHAR2(15)  DEFAULT 'MID_TECH' NOT NULL
                               CONSTRAINT CHK_BUILDS_TECH_STYLE
                               CHECK (TECH_STYLE IN ('FAST_TECH', 'MID_TECH', 'LATE_TECH')),
    MAX_TIER     NUMBER(1)     DEFAULT 3 NOT NULL
                               CONSTRAINT CHK_BUILDS_MAX_TIER
                               CHECK (MAX_TIER IN (1, 2, 3)),
    SCRIPT_CONTENT CLOB,
    CREATED_AT   DATE          DEFAULT SYSDATE,
    CONSTRAINT UK_USER_BUILD_NAME_RACE UNIQUE (USER_ID, BUILD_NAME, RACE, VS_RACE)
);
COMMENT ON COLUMN TBL_BUILDS.PLAY_STYLE   IS '플레이스타일: HARASS_FOCUS=견제 / DEFENSIVE=방어 / AGGRESSIVE=공격';
COMMENT ON COLUMN TBL_BUILDS.EXPAND_STYLE IS '첫멀티타이밍: FAST_MULTI=패스트(즉시) / NORMAL_MULTI=일반(5분) / LATE_MULTI=늦은(10분)';
COMMENT ON COLUMN TBL_BUILDS.AGGRESSION   IS '확장성향: MIN_MULTI=최소 / MID_MULTI=중간 / MAX_MULTI=최대';
COMMENT ON COLUMN TBL_BUILDS.TECH_STYLE   IS '테크성향: FAST_TECH=빠른(5분) / MID_TECH=일반(10분) / LATE_TECH=느린(15분)';
COMMENT ON COLUMN TBL_BUILDS.MAX_TIER     IS '최대 티어: 1=1티어만 / 2=1~2티어 / 3=전체(기본)';

-- ★ TBL_BUILDS 생성 후 TBL_PVE_OPPONENTS의 BUILD_ID FK 추가
ALTER TABLE TBL_PVE_OPPONENTS
ADD CONSTRAINT FK_PVE_OPP_BUILD FOREIGN KEY (BUILD_ID) REFERENCES TBL_BUILDS(BUILD_ID);

