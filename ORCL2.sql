-- 1. 사용자
CREATE TABLE TBL_USERS (
    USER_ID     VARCHAR2(50)  PRIMARY KEY,
    USER_PW     VARCHAR2(100) NOT NULL,
    USER_NICK   VARCHAR2(100) NOT NULL,
    CRYSTAL     NUMBER        DEFAULT 0,
    TEAM_NAME   VARCHAR2(100),
    TRAIN_POINT NUMBER        DEFAULT 0
);

-- 2. 선수 마스터 (STAT_MACRO/STAT_MICRO/STAT_LUCK → STAT_HP/STAT_HARASS/STAT_SPEED 로 컬럼명 변경 반영)
CREATE TABLE TBL_PLAYERS (
    PLAYER_SEQ      NUMBER        CONSTRAINT PK_PLAYERS PRIMARY KEY,
    PLAYER_NAME     VARCHAR2(100) NOT NULL,
    RACE            CHAR(1)       NOT NULL CHECK (RACE IN ('T', 'P', 'Z')),
    RARITY          VARCHAR2(10)  NOT NULL CHECK (RARITY IN ('N', 'R', 'SR', 'SSR', 'UR')),
    STAT_ATTACK     NUMBER(3)     DEFAULT 50,
    STAT_DEFENSE    NUMBER(3)     DEFAULT 50,
    STAT_HP         NUMBER(3)     DEFAULT 50,
    STAT_HARASS     NUMBER(3)     DEFAULT 50,
    STAT_SPEED      NUMBER(3)     DEFAULT 50,
    PLAYER_IMG_URL  VARCHAR2(255),
    PLAYER_COST     NUMBER(3)     DEFAULT 10
);

-- 3. 시즌 팩
CREATE TABLE TBL_PACKS (
    PACK_SEQ        NUMBER        CONSTRAINT PK_PACKS PRIMARY KEY,
    PACK_NAME       VARCHAR2(100) NOT NULL UNIQUE,
    DESCRIPTION     VARCHAR2(500),
    COST_CRYSTAL    NUMBER        DEFAULT 0,
    BANNER_IMG_URL  VARCHAR2(255),
    IS_AVAILABLE    CHAR(1)       DEFAULT 'Y' CHECK (IS_AVAILABLE IN ('Y', 'N'))
);

-- 4. 팩 구성 선수 (확률표)
CREATE TABLE TBL_PACK_CONTENTS (
    PACK_CONTENT_SEQ NUMBER       CONSTRAINT PK_PACK_CONTENTS PRIMARY KEY,
    PACK_SEQ         NUMBER       NOT NULL CONSTRAINT FK_PACK_CONTENTS_PACK   REFERENCES TBL_PACKS(PACK_SEQ),
    PLAYER_SEQ       NUMBER       NOT NULL CONSTRAINT FK_PACK_CONTENTS_PLAYER REFERENCES TBL_PLAYERS(PLAYER_SEQ),
    DRAW_PROBABILITY NUMBER(7, 6) NOT NULL CHECK (DRAW_PROBABILITY > 0 AND DRAW_PROBABILITY <= 1),
    CONSTRAINT UK_PACK_PLAYER UNIQUE (PACK_SEQ, PLAYER_SEQ)
);

-- 5. 유저 보유 선수 (CONDITION/WIN_STREAK/ENHANCE_* 컬럼 추가 및 ENHANCE_MACRO 등 컬럼명 변경 반영)
CREATE TABLE TBL_OWNED_PLAYERS (
    OWNED_PLAYER_SEQ       NUMBER       CONSTRAINT PK_OWNED_PLAYERS PRIMARY KEY,
    USER_ID                VARCHAR2(50) NOT NULL CONSTRAINT FK_OWNED_PLAYER_USER   REFERENCES TBL_USERS(USER_ID),
    PLAYER_SEQ             NUMBER       NOT NULL CONSTRAINT FK_OWNED_PLAYER_MASTER REFERENCES TBL_PLAYERS(PLAYER_SEQ),
    CURRENT_ATTACK         NUMBER(3)    DEFAULT NULL,
    CURRENT_DEFENSE        NUMBER(3)    DEFAULT NULL,
    CURRENT_HP             NUMBER(3)    DEFAULT NULL,
    CURRENT_HARASS         NUMBER(3)    DEFAULT NULL,
    CURRENT_SPEED          NUMBER(3)    DEFAULT NULL,
    ACQUIRED_AT            DATE         DEFAULT SYSDATE,
    CURRENT_RARITY         VARCHAR2(10) NOT NULL,
    ACQUIRED_FROM_PACK_SEQ NUMBER       CONSTRAINT FK_OWNED_PLAYER_PACK REFERENCES TBL_PACKS(PACK_SEQ),
    CONDITION              VARCHAR2(10) DEFAULT 'NORMAL' CHECK (CONDITION IN ('PEAK', 'GOOD', 'NORMAL', 'TIRED', 'WORST')),
    WIN_STREAK             NUMBER(3)    DEFAULT 0,
    ENHANCE_LEVEL          NUMBER(2)    DEFAULT 0,
    ENHANCE_ATTACK         NUMBER       DEFAULT 0,
    ENHANCE_DEFENSE        NUMBER       DEFAULT 0,
    ENHANCE_HP             NUMBER       DEFAULT 0,
    ENHANCE_HARASS         NUMBER       DEFAULT 0,
    ENHANCE_SPEED          NUMBER       DEFAULT 0,
    ENHANCE_STREAK         NUMBER       DEFAULT 0
);

-- 6. 경기 기록
CREATE TABLE TBL_MATCH_RECORDS (
    MATCH_SEQ        NUMBER       CONSTRAINT PK_MATCH_RECORDS PRIMARY KEY,
    OWNED_PLAYER_SEQ NUMBER       NOT NULL CONSTRAINT FK_MATCH_RECORDS_OWNED REFERENCES TBL_OWNED_PLAYERS(OWNED_PLAYER_SEQ) ON DELETE CASCADE,
    MATCH_TYPE       VARCHAR2(20) DEFAULT 'PVE',
    OPPONENT_NAME    VARCHAR2(100),
    OPPONENT_RACE    VARCHAR2(10),
    MAP_NAME         VARCHAR2(100),
    IS_WIN           CHAR(1)      NOT NULL CHECK (IS_WIN IN ('Y', 'N')),
    MATCH_DATE       DATE         DEFAULT SYSDATE
);

-- 7. PVE 엔트리 (1군) : SLOT_NUMBER 범위 1~7 → 1~9 로 확장 (3:3 BO3, 세트당 3명 x 3세트 = 9슬롯)
--    + FK에 ON DELETE CASCADE 반영
CREATE TABLE TBL_PVE_ENTRY (
    PVE_ENTRY_SEQ    NUMBER       CONSTRAINT PK_PVE_ENTRY PRIMARY KEY,
    USER_ID          VARCHAR2(50) NOT NULL CONSTRAINT FK_PVE_ENTRY_USER         REFERENCES TBL_USERS(USER_ID),
    OWNED_PLAYER_SEQ NUMBER       NOT NULL CONSTRAINT FK_PVE_ENTRY_OWNED_PLAYER REFERENCES TBL_OWNED_PLAYERS(OWNED_PLAYER_SEQ) ON DELETE CASCADE,
    SLOT_NUMBER      NUMBER(1)    NOT NULL CONSTRAINT CHK_PVE_SLOT_NUM CHECK (SLOT_NUMBER BETWEEN 1 AND 9),
    CONSTRAINT UK_USER_SLOT         UNIQUE (USER_ID, SLOT_NUMBER),
    CONSTRAINT UK_USER_OWNED_PLAYER UNIQUE (USER_ID, OWNED_PLAYER_SEQ)
);

-- 8. PVE 메인 진행
CREATE TABLE USER_PVE_PROGRESS (
    USER_ID      VARCHAR2(50) NOT NULL,
    STAGE_LEVEL  NUMBER       NOT NULL,
    STAGE_STATUS VARCHAR2(20) NOT NULL,
    UPDATED_AT   TIMESTAMP    DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT PK_USER_PVE_PROGRESS     PRIMARY KEY (USER_ID, STAGE_LEVEL),
    CONSTRAINT FK_USER_PVE_PROGRESS_USER FOREIGN KEY (USER_ID) REFERENCES TBL_USERS(USER_ID) ON DELETE CASCADE,
    CONSTRAINT CHK_USER_PVE_STATUS       CHECK (STAGE_STATUS IN ('CLEARED', 'IN_PROGRESS', 'LOCKED'))
);

-- 9. PVE 서브스테이지 마스터
CREATE TABLE TBL_PVE_SUBSTAGES (
    STAGE_LEVEL        NUMBER        NOT NULL,
    SUB_LEVEL          NUMBER        NOT NULL,
    SUB_TITLE          VARCHAR2(100) NOT NULL,
    OPPONENT_TEAM_NAME VARCHAR2(100),
    CONSTRAINT PK_PVE_SUBSTAGES PRIMARY KEY (STAGE_LEVEL, SUB_LEVEL)
);

-- 10. 사용자 서브스테이지 진행
CREATE TABLE USER_PVE_SUBSTAGE_PROGRESS (
    USER_ID     VARCHAR2(50) NOT NULL,
    STAGE_LEVEL NUMBER       NOT NULL,
    SUB_LEVEL   NUMBER       NOT NULL,
    IS_CLEARED  CHAR(1)      DEFAULT 'Y' NOT NULL CHECK (IS_CLEARED = 'Y'),
    CLEARED_AT  TIMESTAMP    DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT PK_USER_PVE_SUBSTAGE       PRIMARY KEY (USER_ID, STAGE_LEVEL, SUB_LEVEL),
    CONSTRAINT FK_USER_PVE_SUBSTAGE_USER   FOREIGN KEY (USER_ID) REFERENCES TBL_USERS(USER_ID) ON DELETE CASCADE,
    CONSTRAINT FK_USER_PVE_SUBSTAGE_MASTER FOREIGN KEY (STAGE_LEVEL, SUB_LEVEL) REFERENCES TBL_PVE_SUBSTAGES(STAGE_LEVEL, SUB_LEVEL) ON DELETE CASCADE
);
CREATE INDEX IDX_USER_PVE_SUBSTAGE_USER ON USER_PVE_SUBSTAGE_PROGRESS (USER_ID, STAGE_LEVEL);

-- 11. 맵 마스터 (MAP_IMG_URL 컬럼 추가 반영)
CREATE TABLE TBL_MAPS (
    MAP_ID      VARCHAR2(50)   CONSTRAINT PK_MAPS PRIMARY KEY,
    MAP_NAME    VARCHAR2(100)  NOT NULL,
    DESCRIPTION VARCHAR2(1000),
    WIN_RATE_T  NUMBER(5, 2)   DEFAULT 50.00,
    WIN_RATE_P  NUMBER(5, 2)   DEFAULT 50.00,
    WIN_RATE_Z  NUMBER(5, 2)   DEFAULT 50.00,
    MAP_IMG_URL VARCHAR2(500)
);

-- 12. PVE 서브스테이지별 맵 : 3:3 BO3 체계 → SET_NUMBER 는 실제 경기 세트(1~3)만 사용
--     + FK에 ON DELETE CASCADE 반영
CREATE TABLE TBL_PVE_SUBSTAGE_MAPS (
    STAGE_LEVEL NUMBER       NOT NULL,
    SUB_LEVEL   NUMBER       NOT NULL,
    SET_NUMBER  NUMBER(1)    NOT NULL,
    MAP_ID      VARCHAR2(50) NOT NULL,
    CONSTRAINT PK_PVE_SUBSTAGE_MAPS    PRIMARY KEY (STAGE_LEVEL, SUB_LEVEL, SET_NUMBER),
    CONSTRAINT FK_SUBSTAGE_MAPS_MASTER FOREIGN KEY (STAGE_LEVEL, SUB_LEVEL) REFERENCES TBL_PVE_SUBSTAGES(STAGE_LEVEL, SUB_LEVEL) ON DELETE CASCADE,
    CONSTRAINT FK_SUBSTAGE_MAPS_MAP    FOREIGN KEY (MAP_ID) REFERENCES TBL_MAPS(MAP_ID) ON DELETE CASCADE
);
COMMENT ON COLUMN TBL_PVE_SUBSTAGE_MAPS.SET_NUMBER IS '실제 경기 세트 번호 (1~3, BO3). TBL_PVE_OPPONENTS.SET_NUMBER(1~9 슬롯)와는 다른 개념.';

-- 13. 빌드 마스터 (TECH_STYLE/EXPAND_STYLE 컬럼 제거, MAX_BASES/FOCUS_ATTACK_TIME/HARASS_STYLE/
--     PREFERRED_BUILDINGS/BUILD_PRESET/WIN_COUNT/LOSE_COUNT 추가, RACE/VS_RACE VARCHAR2(10)로 변경,
--     VS_RACE NULL 허용 및 체크 제거, USER_ID FK 제거(SYSTEM 빌드 지원), PLAY_STYLE/AGGRESSION
--     체크 값 최종본 반영 — 모두 최종 상태로 흡수)
CREATE TABLE TBL_BUILDS (
    BUILD_ID            NUMBER        CONSTRAINT PK_BUILDS PRIMARY KEY,
    USER_ID             VARCHAR2(50),
    BUILD_NAME          VARCHAR2(100) NOT NULL,
    RACE                VARCHAR2(10)  NOT NULL CONSTRAINT CHK_BUILDS_RACE CHECK (RACE IN ('T', 'P', 'Z')),
    VS_RACE             VARCHAR2(10)  NULL,
    PLAY_STYLE          VARCHAR2(15)  DEFAULT 'AGGRESSIVE' NOT NULL
                                       CONSTRAINT CHK_BUILDS_PLAY_STYLE
                                       CHECK (PLAY_STYLE IN ('AGGRESSIVE', 'NORMAL', 'DEFENSIVE')),
    AGGRESSION          VARCHAR2(15)  DEFAULT 'NORMAL_MULTI' NOT NULL
                                       CONSTRAINT CHK_BUILDS_AGGRESSION
                                       CHECK (AGGRESSION IN ('FAST_MULTI', 'NORMAL_MULTI', 'SLOW_MULTI')),
    MAX_TIER            NUMBER(1)     DEFAULT 3 NOT NULL
                                       CONSTRAINT CHK_BUILDS_MAX_TIER
                                       CHECK (MAX_TIER IN (1, 2, 3)),
    MAX_BASES           NUMBER(2)     DEFAULT 4 NOT NULL,
    FOCUS_ATTACK_TIME   NUMBER(5)     DEFAULT 0 NOT NULL,
    HARASS_STYLE        VARCHAR2(20)  DEFAULT 'NORMAL_HARASS'
                                       CONSTRAINT CK_BUILDS_HARASS_STYLE
                                       CHECK (HARASS_STYLE IN ('NO_HARASS', 'NORMAL_HARASS', 'HEAVY_HARASS')),
    PREFERRED_BUILDINGS VARCHAR2(500),
    BUILD_PRESET        VARCHAR2(100),
    WIN_COUNT           NUMBER        DEFAULT 0,
    LOSE_COUNT          NUMBER        DEFAULT 0,
    SCRIPT_CONTENT      CLOB,
    CREATED_AT          DATE          DEFAULT SYSDATE,
    CONSTRAINT UK_USER_BUILD_NAME_RACE UNIQUE (USER_ID, BUILD_NAME, RACE, VS_RACE)
);
COMMENT ON COLUMN TBL_BUILDS.PLAY_STYLE   IS '플레이스타일: AGGRESSIVE=공격 / NORMAL=일반 / DEFENSIVE=방어';
COMMENT ON COLUMN TBL_BUILDS.AGGRESSION   IS '확장성향: FAST_MULTI=빠른멀티 / NORMAL_MULTI=일반멀티 / SLOW_MULTI=느린멀티';
COMMENT ON COLUMN TBL_BUILDS.MAX_TIER     IS '최대 티어: 1=1티어만 / 2=1~2티어 / 3=전체(기본)';
COMMENT ON COLUMN TBL_BUILDS.HARASS_STYLE IS '견제 성향: NO_HARASS(견제없음) / NORMAL_HARASS(일반견제, 2~6회) / HEAVY_HARASS(강한견제, 7~11회)';

-- 14. 페이즈별 유닛 구성
CREATE TABLE TBL_BUILD_UNITS (
    BUILD_UNIT_ID NUMBER       CONSTRAINT PK_BUILD_UNITS PRIMARY KEY,
    BUILD_ID      NUMBER       NOT NULL CONSTRAINT FK_BU_BUILD REFERENCES TBL_BUILDS(BUILD_ID) ON DELETE CASCADE,
    PHASE         VARCHAR2(10) NOT NULL CHECK (PHASE IN ('EARLY', 'MID', 'LATE')),
    UNIT_ID       VARCHAR2(50) NOT NULL,
    PRIORITY      NUMBER(1)    DEFAULT 1 NOT NULL CHECK (PRIORITY BETWEEN 1 AND 5),
    CONSTRAINT UK_BUILD_PHASE_UNIT UNIQUE (BUILD_ID, PHASE, UNIT_ID)
);
COMMENT ON COLUMN TBL_BUILD_UNITS.PRIORITY IS '1=최우선(가장많이생산) ~ 5=보조';

-- 15. 선수-빌드 연결
CREATE TABLE TBL_OWNED_BUILDS (
    OWNED_BUILD_SEQ NUMBER NOT NULL CONSTRAINT PK_OWNED_BUILDS PRIMARY KEY,
    PLAYER_SEQ      NUMBER NOT NULL CONSTRAINT FK_OB_PLAYER REFERENCES TBL_OWNED_PLAYERS(OWNED_PLAYER_SEQ) ON DELETE CASCADE,
    BUILD_ID        NUMBER NOT NULL CONSTRAINT FK_OB_BUILD  REFERENCES TBL_BUILDS(BUILD_ID) ON DELETE CASCADE,
    CONSTRAINT UK_PLAYER_BUILD UNIQUE (PLAYER_SEQ, BUILD_ID)
);
COMMENT ON TABLE TBL_OWNED_BUILDS IS '보유선수(OWNED_PLAYER)가 사용하는 빌드 연결';

-- 16. PVE AI 엔트리 : 구 BUILD_ID 단일 컬럼 제거 → 상대 종족별 빌드(BUILD_ID_VS_T/Z/P) 3개로 분리.
--     SET_NUMBER 는 3:3 BO3 슬롯(1~9) 의미로 사용 (1~3=1세트 출전 3명, 4~6=2세트, 7~9=3세트)
CREATE TABLE TBL_PVE_OPPONENTS (
