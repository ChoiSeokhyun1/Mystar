-- 1. 일일 미션 마스터 테이블 (미션 정의)
CREATE TABLE TBL_DAILY_MISSIONS (
    MISSION_ID          NUMBER CONSTRAINT PK_DAILY_MISSIONS PRIMARY KEY,
    MISSION_TYPE        VARCHAR2(50) NOT NULL,  -- 'PVE_WIN', 'PVE_PLAY', 'GACHA', 'ENHANCE', 'LOGIN'
    MISSION_TITLE       VARCHAR2(200) NOT NULL,
    MISSION_DESC        VARCHAR2(500),
    TARGET_COUNT        NUMBER DEFAULT 1,       -- 목표 달성 횟수
    REWARD_CRYSTAL      NUMBER DEFAULT 0,       -- 보상 크리스탈
    REWARD_EXP          NUMBER DEFAULT 0,       -- 보상 경험치 (향후 레벨 시스템용)
    MISSION_ICON        VARCHAR2(50),           -- 아이콘 이모지
    DISPLAY_ORDER       NUMBER DEFAULT 0,       -- 표시 순서
    IS_ACTIVE           CHAR(1) DEFAULT 'Y' CHECK (IS_ACTIVE IN ('Y', 'N')),
    CREATED_AT          DATE DEFAULT SYSDATE
);

-- 2. 유저별 일일 미션 진행 상황 테이블
CREATE TABLE TBL_USER_DAILY_MISSIONS (
    USER_ID             VARCHAR2(50) NOT NULL,
    MISSION_ID          NUMBER NOT NULL,
    CURRENT_COUNT       NUMBER DEFAULT 0,       -- 현재 진행 횟수
    IS_COMPLETED        CHAR(1) DEFAULT 'N' CHECK (IS_COMPLETED IN ('Y', 'N')),
    IS_CLAIMED          CHAR(1) DEFAULT 'N' CHECK (IS_CLAIMED IN ('Y', 'N')),
    COMPLETED_AT        DATE,
    CLAIMED_AT          DATE,
    RESET_DATE          DATE DEFAULT TRUNC(SYSDATE), -- 마지막 리셋 날짜
    CONSTRAINT PK_USER_DAILY_MISSIONS PRIMARY KEY (USER_ID, MISSION_ID),
    CONSTRAINT FK_USER_DAILY_USER FOREIGN KEY (USER_ID) REFERENCES TBL_USERS(USER_ID) ON DELETE CASCADE,
    CONSTRAINT FK_USER_DAILY_MISSION FOREIGN KEY (MISSION_ID) REFERENCES TBL_DAILY_MISSIONS(MISSION_ID) ON DELETE CASCADE
);

-- 인덱스 생성
CREATE INDEX IDX_USER_DAILY_RESET ON TBL_USER_DAILY_MISSIONS(USER_ID, RESET_DATE);
CREATE INDEX IDX_DAILY_MISSION_TYPE ON TBL_DAILY_MISSIONS(MISSION_TYPE);

